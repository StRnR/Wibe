package com.mahaventures.wibe.Models;

class UserRole {
    private int User;
    private int Role;
    private String Name;
    private String Description;

    public int getUser() {
        return User;
    }

    public int getRole() {
        return Role;
    }

    public String getName() {
        return Name;
    }

    public String getDescription() {
        return Description;
    }
}
